# Tipos de Datos Enteros

numero =  10   # Numero entero
print(f" El numero es de tipo {type(numero)} ")

#Numeros Binarios

numero_binario = 0b0011110  #  La literal de octal es  '0b' 01
print(numero_binario)
print(f" El numero binario es de tipo {type(numero_binario)} ")


#Numero Octal
numero_octal = 0o012  #  La literal de octal es  '0o'  0.....7
print(numero_octal)
print(f" El numero es octal de tipo {type(numero_octal)} ")

#Numero Hexadecimal  # 0.....9 A F
numero_hex = 0xC
print(numero_hex)
print(f" El numero es Hex de tipo {type(numero_hex)} ")


