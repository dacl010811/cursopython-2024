


def suma(numero_1, numero_2, numero_3):       # Definicion de los parametros de la funcion
    return numero_1 + numero_2 + numero_3

def resta(numero_1, numero_2):
    return  numero_1 - numero_2

def multiplicacion(numero_1, numero_2):
    return numero_1 * numero_2

def division(dividendo, divisor):
    return dividendo / divisor


# Invocarlas a las funciones
# Invocacion de funciones mediante argumentos por posicion

print(f"El resultado de la suma es =  {suma(5,4.0,3.0)}")

print(f"El resultado de la resta es =  {resta(500,40.0)}")

print(f"El resultado de la resta es =  {multiplicacion(5,8)}")

print(f"El resultado de la resta es =  {division(10,2)}")

